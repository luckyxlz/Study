package SnakeGame;


public class Node {
    private int x;      //节点的横坐标
    private int y;      //节点的纵坐标

    //空构造器
    public Node() {
    }

    //带参构造器
    public Node(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //获得x坐标
    public int getX() {
        return x;
    }

    //获得y坐标
    public int getY() {
        return y;
    }






}
